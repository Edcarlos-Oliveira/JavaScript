// Contador com 'while':
console.log('Contagem com "while":')
var c = 1
while (c <= 5) {
    console.log(c)
    c++
}

console.log('Contagem com "do while":') // Para pular uma linha

// Contador com 'do while':
var cont = 1
do {
    console.log(cont)
    cont++
}while (cont <= 6)

console.log('Contagem com "for":')

// Contador com 'for':
for(var cont2 = 1; cont2 <= 7; cont2++) {
    console.log(cont2)
}

